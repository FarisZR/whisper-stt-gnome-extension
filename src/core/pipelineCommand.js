export function buildRecordingCommand(outputPath) {
    return [
        'gst-launch-1.0',
        '-m',
        'pulsesrc',
        '!',
        'tee',
        'name=t',
        't.',
        '!',
        'queue',
        '!',
        'audioconvert',
        '!',
        'wavenc',
        '!',
        'filesink',
        `location=${outputPath}`,
        't.',
        '!',
        'queue',
        '!',
        'audioconvert',
        '!',
        'level',
        'interval=100000000',
        'post-messages=true',
        '!',
        'fakesink',
    ];
}
